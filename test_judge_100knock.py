    def run_all_tests(self):
        """100個すべてのテストを実行"""
        print("\n【実行中】100個のテストケースを実行...")
        print("-" * 70)

        for i, case in enumerate(self.test_cases, 1):
            name, context, request, expected = case
            result = self.judge.judge({
                "context": context,
                "judgment_request": request
            })

            actual_decision = result.get("decision")
            is_correct = actual_decision == expected

            self.results.append({
                "index": i,
                "name": name,
                "expected": expected,
                "actual": actual_decision,
                "score": result.get("score"),
                "correct": is_correct
            })

            # 進捗表示
            if i % 10 == 0:
                print(f"進捗: {i}/100 完了")

        print("進捗: 100/100 完了")

    def analyze_results(self):
        """テスト結果を分析"""
        print("\n" + "=" * 70)
        print("【100ノック精度分析結果】")
        print("=" * 70)

        total = len(self.results)
        correct = sum(1 for r in self.results if r["correct"])
        accuracy = correct / total * 100

        # カテゴリ別の精度
        categories = {
            "ポジティブ": self.results[0:20],
            "ネガティブ": self.results[20:40],
            "境界ケース": self.results[40:60],
            "ドメイン別": self.results[60:80],
            "テキスト長": self.results[80:100],
        }

        print(f"\n【総合精度】")
        print(f"  正解数: {correct}/100")
        print(f"  精度: {accuracy:.1f}%")

        print(f"\n【カテゴリ別精度】")
        for category_name, category_results in categories.items():
            cat_correct = sum(1 for r in category_results if r["correct"])
            cat_accuracy = cat_correct / len(category_results) * 100
            print(f"  {category_name:12s}: {cat_correct:2d}/20 ({cat_accuracy:5.1f}%)")

        # 誤った判定の詳細
        errors = [r for r in self.results if not r["correct"]]
        if errors:
            print(f"\n【誤った判定】(全 {len(errors)} 件)")
            print("-" * 70)
            for error in errors[:10]:  # 最初の10件を表示
                print(f"  {error['index']:2d}. {error['name']:20s}: "
                      f"期待 {error['expected']:3s} → 実際 {error['actual']:3s} "
                      f"(Score: {error['score']})")
            if len(errors) > 10:
                print(f"  ... 他 {len(errors) - 10} 件")

        # 信頼性レベル
        print(f"\n【信頼性レベル評価】")
        if accuracy >= 95:
            level = "⭐⭐⭐⭐⭐ (5/5) - 超高精度"
        elif accuracy >= 90:
            level = "⭐⭐⭐⭐⭐ (5/5) - エンタープライズレベル"
        elif accuracy >= 85:
            level = "⭐⭐⭐⭐ (4/5) - 本番環境対応"
        elif accuracy >= 80:
            level = "⭐⭐⭐⭐ (4/5) - 実用的"
        elif accuracy >= 70:
            level = "⭐⭐⭐ (3/5) - 改善の余地あり"
        else:
            level = "⭐⭐ (2/5) - 要改善"

        print(f"  精度: {accuracy:.1f}%")
        print(f"  レベル: {level}")

        print("\n" + "=" * 70)
        print("✅ 100ノック精度測定完了")
        print("=" * 70)

        return accuracy


